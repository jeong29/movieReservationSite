package com.spring.service.vo;

import java.util.List;

public class GenreVO {
	private int genreNum;
	private String genreName;
	
	private List<Integer> genreNumList;
	
	// 정렬을 위한 변수
	private int rowNum;
	
	public int getRowNum() {
		return rowNum;
	}
	public void setRowNum(int rowNum) {
		this.rowNum = rowNum;
	}
	public List<Integer> getGenreNumList() {
		return genreNumList;
	}
	public void setGenreNumList(List<Integer> genreNumList) {
		this.genreNumList = genreNumList;
	}
	public int getGenreNum() {
		return genreNum;
	}
	public void setGenreNum(int genreNum) {
		this.genreNum = genreNum;
	}
	public String getGenreName() {
		return genreName;
	}
	public void setGenreName(String genreName) {
		this.genreName = genreName;
	}
	
}
